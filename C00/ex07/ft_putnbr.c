#include <unistd.h>

void ft_putnbr(int nb)
{
    if (nb < 0)
    {
        write(1, "-", 1);
        nb = -nb;
    }
    if (nb >= 10)
    {
        ft_putnbr(nb / 10);
        ft_putnbr(nb % 10);
    }
    else 
    {
        char c = '0' + nb;
        write(1, &c, 1);
    }
}